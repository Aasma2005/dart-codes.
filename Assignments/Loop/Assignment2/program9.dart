void main(){
  int grade=79;

  if(grade>=0 && grade<=25){
    print("grade D");
  }
  else if(grade>=25 && grade<=50){
    print("Grade C");
  }
  else if(grade>=50 && grade<=75){
    print("Grade B");
  }
  else if(grade>=75 && grade<=100){
    print("Grade A");
  }
  else{
    print("fail");
  }
}
